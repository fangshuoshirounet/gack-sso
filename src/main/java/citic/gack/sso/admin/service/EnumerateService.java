package citic.gack.sso.admin.service;

import java.util.List;

import citic.gack.sso.base.PageInfo;
import citic.gack.sso.base.exception.SysException;
import citic.gack.sso.entity.SysEnumerate;

public interface EnumerateService {
	/**
	 * 
	 * @param entity
	 * @return 插入记录
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public SysEnumerate insert(SysEnumerate entity) throws SysException;

	/**
	 * 
	 * @param entity
	 * @return 修改记录
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public int update(SysEnumerate entity) throws SysException;

	/**
	 * 
	 * @param entity
	 * @return 删除记录
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public void delete(SysEnumerate entity) throws SysException;

	/**
	 * 
	 * @param entity
	 * @return 查询 集合
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public List<SysEnumerate> queryList(SysEnumerate entity) throws SysException;

	/**
	 * 
	 * @param entity
	 * @return 查询对象
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public SysEnumerate queryBean(SysEnumerate entity) throws SysException;

	/**
	 * 
	 * @param entity
	 * @param pageInfo
	 * @return 分页查询
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public PageInfo queryListByPage(SysEnumerate entity, PageInfo pageInfo) throws SysException;

	/**
	 * 
	 * @param delList
	 * @return 批量删除记录
	 * @throws SysException
	 * @throws AppException
	 * @author wangjincheng
	 */
	public void batchDelete(List<SysEnumerate> delList) throws SysException;
}